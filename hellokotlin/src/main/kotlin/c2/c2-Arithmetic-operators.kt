// Arithmetic operators demo.
fun main() {
    println(2 + 2)
    println(2 - 2)
    println(2 * 2)
    println(2 / 2)
    println(22 / 7)
    println(22.0 / 7.0)
    println(28 % 10)
    println(28.0 % 10.0)
    println("%f".format(28.0 % 10.0))
    println("%.0f".format(28.0 % 10.0))
    // shift operations
    println(1 shl 3)
    println(32 shr 2)
}